                      <h2>Fencing Mid South - What We Do</h2>
                        <p><strong>Together with Fencing New Zealand we promote and administer the art and sport of fencing.</strong></p>
						<p>Fencing Mid South is the regional body responsible for the promotion and administration of fencing in the upper 
							half of the South Island. We are one of four regional bodies in New Zealand, underneath <a href="http://www.fencing.org.nz/" target="_blank">Fencing New Zealand ("FeNZ")</a>.</p>
						<p>Fencing Mid South is governed by an elected committee (a Chairperson, Treasurer, etc), plus a representative from each <a href="Clubs.php">registered club</a>. Fencers who want to fence in official competitions pay an annual affiliation fee to Fencing New Zealand which makes them members of Mid South Fencing.</p>
						<p>The President of Mid South sits on the Fencing New Zealand governance board, and Mid South is also represented at the national level as selectors and on referee panels.</p>
						<p>In our region, the main thing Fencing Mid South does is organise competitions. There is an annual cycle of competitions, which is part of the national fencing calendar. The four NZ fencing regions coordinate their competitions and take turn hosting some of the movable competitions, to ensure a competitive season for all fencers.</p>
						<p>Mid South Fencing is well represented at both National and International competition levels with fencers representing New Zealand at Open, Veteran, Junior and Cadet levels.</p>
