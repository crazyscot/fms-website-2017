<h2>AGM 2017</h2>
<p>The Fencing Mid South AGM for 2017 is happening on Saturday 25th March, 6pm at the Fencing Institute.</a></p>
<p>We will start with a barbeque, followed by the presentation of the Fencing Mid South trophies and awards for 2016.</p>
<p>All fencers in the region are encourage to attend, to celebrate our successes of 2016, and 
be part of the planning for 2017. Fencing in the region only happens because we all work together 
to make it happen. If you think you can help with one of the positions listed below (or in any other way), please speak up!</p>
