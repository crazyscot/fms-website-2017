<h1>Presidents Cup</h1>
<p>
	The Presidents Cup is a FENZ comptition, not a Mid-South competition. Each of the four 
	fencing regions takes turns at hosting the event.
</p>
<p>
	The Presidents Cup is a relatively recent competition. It was started in order to provide
	an opportunity for fencers to attend a second national-level open competition, in addition to
	Nationals. Presidents Cup is usually held around the beginning of the season, whereas Nationals 
	is held at Labour Weekend.
</p>
<p>
</p>
