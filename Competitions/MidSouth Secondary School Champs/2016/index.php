					<h2>Summary of details</h2>
<p dir="ltr"><u><strong>Fencing &nbsp;MidSouth Secondary School&nbsp;Championships</strong></u></p>

<p dir="ltr">This competition&nbsp;is open to all fencers who are&nbsp;secondary school age ie: Year 9 to Year 13</p>

<p dir="ltr">Participants must be&nbsp;members of&nbsp;Fencing New Zealand &nbsp;(ie: have paid their&nbsp;membership fee for the 2016 year). If you are not yet a member you can click on the link below and become one and pay online. ($65)</p>

<p dir="ltr"><a href="https://www.revolutionise.com.au/fencingmidsouth/registration/">https://www.revolutionise.com.au/fencingmidsouth/registration/</a></p>

<p dir="ltr">&nbsp;</p>

<p dir="ltr"><strong>Format: </strong>This&nbsp;competition will have both&nbsp;individual and school team events.&nbsp;&nbsp;Foil, Epee and Sabre will be fenced.</p>

<p dir="ltr"><strong>Individual Competition:&nbsp;</strong>&nbsp;First Round:&nbsp;poules with bouts to 5 hits. Second Round: Direct Elimination to 15 hits. &nbsp;</p>

<p dir="ltr">Separate&nbsp;events will be held for Boys and Girls as long as there are 6+ competitors&nbsp;in each gender. So encourage your school mates to come to this competition too.</p>

<p dir="ltr"><strong>Teams Event</strong> - 3 fencers per team + 1 reserve.(teams do not need to have a reserve) &nbsp;Format will depend on how many teams are entered. Teams may be all boys/girls or mixed as long as fencers are all from the same school. Teams will be ranked from placings in the individual event.</p>

<p dir="ltr"><strong>Entry Fee:</strong>&nbsp;</p>

<p dir="ltr"><strong>Individual Competition:</strong>&nbsp;$22 per weapon. Entries close Wednesday 17th&nbsp;August, 9pm.&nbsp;Late entries will be accepted at the discretion of the organiser.&nbsp;</p>

<p dir="ltr">Entry fees are to be paid by internet banking, &nbsp;a/c number 03-0802-0087494-00. &nbsp;Please include &ldquo;fencer&rsquo;s name, &nbsp;Secondary School champs&rdquo; as the reference.</p>

<p dir="ltr"><strong>Team Competition: </strong>$15 if there are 3 team members, $20 if there are 4 team members.</p>

<p dir="ltr">Entry Fee for team competition is to be paid on the day when team members are known. As this event is to be paid at the competition and the school/ coaches have input into which fencers will be in the team competition when registering you are indicating that you would like to be in one of your schools teams. Hence on the registration form it says there is no payment for this event. If you are selected for your school team you will pay your $5 on the day.&nbsp;</p>

<p><strong>Gear Requirements:&nbsp;</strong>Minimum 350N jacket, 350N plastron,&nbsp; 350N breeches or tracksuit pants, 350N mask, 350N&nbsp;glove. Chest protectors are compulsory&nbsp;for girls. Lame Foil bibs are not required. &nbsp;Standard (non-FIE) blades are acceptable. &nbsp;</p>

<p dir="ltr"><strong>Gear is available&nbsp;for hire: </strong>please contact Leasa&nbsp;Ogilvie&nbsp;or your coach about gear hire. leasaogilvie@gmail.com</p>

<p dir="ltr"><strong>Canteen:</strong>&nbsp;Snacks, lunch and drinks will be available for the duration of the competition.</p>

<p dir="ltr"><strong>Timetable:&nbsp;</strong></p>

<p dir="ltr"><strong>Friday 19th August:&nbsp;</strong></p>

<p dir="ltr">6pm Sabre Registration Opens</p>

<p dir="ltr">6:30pm&nbsp;Individual Sabre&nbsp;Starts</p>

<p dir="ltr">Sabre&nbsp;Teams will follow the end of the individual competition</p>

<p dir="ltr"><strong>Saturday 20th August:</strong></p>

<p dir="ltr">8.15am&nbsp;Epee registration</p>

<p dir="ltr">8.45am &nbsp;Individual&nbsp;Epee starts</p>

<p dir="ltr">&nbsp;Epee teams will follow the individual competition</p>

<p><strong>Sunday 21st&nbsp;August:</strong></p>

<p dir="ltr">8:30am&nbsp; Boy&rsquo;s Foil registration</p>

<p dir="ltr">9am &nbsp;Boy&rsquo;s Foil starts</p>

<p dir="ltr">9:30am&nbsp;&nbsp;Girl&#39;s Foil registration*</p>

<p dir="ltr">10am &nbsp;Girl&rsquo;s Foil starts*</p>

<p dir="ltr">&nbsp;Foil Teams will follow the individual competition</p>

<p>* If there are not enough competitors for separate&nbsp;boys and girls events the mixed foil competition will start at the boys foil time. Competitors will be notified by email of this change once entries have closed</p>

<p dir="ltr"><strong>Medals/ Trophies:&nbsp;</strong></p>

<p dir="ltr"><strong>Individual Competition</strong>: Medals&nbsp;will be awarded for first, second, third/ third equal depending on numbers.</p>

<p><strong>Team Competition: </strong>Medals will be awarded for first. Certificates&nbsp;will be given for second and third placed teams.&nbsp;</p>

<p>All trophies will be awarded including the Master of Arms Trophy.</p>
					
