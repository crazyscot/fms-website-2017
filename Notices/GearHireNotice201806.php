<p>
At the recent meeting of the Mid South Executive Council it was agreed 
to rationalise and overhaul the hire charges for competition kit owned 
by Mid South.
</p>

<p>
The following hire fee structure now applies, with effect from 1 July:
<ul>
    <li>An FIE/S2000 weapon and bodywire: $10</li>
    <li>A non-FIE weapon: $5</li>
    <li>Clothing (up to a complete kit, excluding lame): $5</li>
    <li>Lame, foil bib and mask wire: $5</li>
</ul>
</p>

