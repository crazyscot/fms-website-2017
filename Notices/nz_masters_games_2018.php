<h2>NZ Masters Games 2018</h2>
<p>
The New Zealand Masters Games is an annual multisport event which alternates between Dunedin and Wanganui, attracting over 
5000 competitors from New Zealand and overseas.
The Otago Community Trust New Zealand Masters Games 2018 will be held in Dunedin from the 3rd to the 11th of February. 
</p>
<p>
Fencing is a one of the 63 events on offer at the Masters Games. The fencing events are on 10th and 11th February, 
and the minimum age is 40 as of the first day of competition.
</p>
<p>
Please display the <a href="http://fencingmidsouth.org.nz/Notices/Fencing A4 Poster.jpg">attached poster</a> at your clubs, 
and go to <a href="http://www.nzmg.com/" target="_blank">www.nzmg.com</a> 
for more information and registration
</p>
