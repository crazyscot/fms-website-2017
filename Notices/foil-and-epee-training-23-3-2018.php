<h2>Foil and epee training this weekend</h2>
<p>
This weekend at The Institute there will be a training session in foil and epee 
for fencers from secondary school age (13 years old+) to adult,  with at least 
one year of fencing experience.  Physical preparation, footwork practice and LOTS of 
fencing with different people. 
</p>
<p>
