<h1>Commonwealth Vets 2016</h1>
<p>The 2016 Commonwealth Veteran Fencing Champs is to be held in New Zealand, in November this year, 
and Fencing New Zealand has asked Fencing Mid South to host the event.</p>
<p>Hosting an event of this size is a great honor for Fencing Mid South. We are expecting over 200
fencers from all around the world to attend. A committee has been actively planning the event since mid 2015.</p>
<p>Please visit the <a href="http://www.cvfc2016.co.nz/" target="_blank">Commonwealth Vets 2016 </a> web site 
and <a href="https://www.facebook.com/commonwealthveterans2016/" target="_blank">facebook page</a>, 
spread the word, and support the event by participating, helping out, or coming along to see the action!</p>